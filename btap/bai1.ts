function printInfo(names: string, age: number, job: string): void {
    console.log("Name:", names);
    console.log("Age:", age);
    console.log("Job:", job);
}

// Khai báo và gán giá trị cho các biến
let names: string = "a";
let age: number = 1;
let job: string = "b";
printInfo(names, age, job);
